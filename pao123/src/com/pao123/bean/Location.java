package com.pao123.bean;

public class Location {
	private String latitude;
	private String longitude;
	private String haccuracy;
	private String altitude;
	private String vaccuracy;
	private String speed;
	private String timeoffset;
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getHaccuracy() {
		return haccuracy;
	}
	public void setHaccuracy(String haccuracy) {
		this.haccuracy = haccuracy;
	}
	public String getAltitude() {
		return altitude;
	}
	public void setAltitude(String altitude) {
		this.altitude = altitude;
	}
	public String getVaccuracy() {
		return vaccuracy;
	}
	public void setVaccuracy(String vaccuracy) {
		this.vaccuracy = vaccuracy;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getTimeoffset() {
		return timeoffset;
	}
	public void setTimeoffset(String timeoffset) {
		this.timeoffset = timeoffset;
	}
	
}
